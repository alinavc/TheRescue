package com.mycompany.a3;

public class ObjSize {
	private int length;
	private int width;
	
	//constructor
	public ObjSize () {
		length=0;
		width=0;
	}
	//getters
	public int getLength() {
		return length;
	}
	
	public int getWidth() {
		return width;
	}
	//setters
	public void setLength(int newL) {
		length=newL;
	}
	public void setWidth(int newW) {
		width=newW;
	}
}